package com.example.githubdatamicroservice.model;

import java.util.Map;

public class GitHubData {
    private Map<String, Integer> pushCountMap;
    private Map<String, Integer> pullCountMap;
    private String profilePhoto;
    private String fullName;
    private String bio;
    private String since;

    public Map<String, Integer> getPushCountMap() {
        return pushCountMap;
    }

    public void setPushCountMap(Map<String, Integer> pushCountMap) {
        this.pushCountMap = pushCountMap;
    }

    public Map<String, Integer> getPullCountMap() {
        return pullCountMap;
    }

    public void setPullCountMap(Map<String, Integer> pullCountMap) {
        this.pullCountMap = pullCountMap;
    }

    public String getProfilePhoto() {
        return profilePhoto;
    }

    public void setProfilePhoto(String profilePhoto) {
        this.profilePhoto = profilePhoto;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getBio() {
        return bio;
    }

    public void setBio(String bio) {
        this.bio = bio;
    }

    public String getSince() {
        return since;
    }

    public void setSince(String since) {
        this.since = since;
    }
}
